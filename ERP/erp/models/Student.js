import mongoose from 'mongoose';

const studentSchema = new mongoose.Schema({
  roll: {
    type: Number,
    required: true,
    unique: true
  },
  fullName: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  phone: {
    type: String,
    required: true
  },
  isActive: {
    type: Boolean,
    default: true
  }
}, {
  timestamps: true
});

export default mongoose.models.Student || mongoose.model('Student', studentSchema);
